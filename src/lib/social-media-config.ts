/**
 * Détecte la plateforme à partir du champ `icon` ou `name` stocké en DB.
 * Retourne null si aucune correspondance → affiche un bouton générique.
 */
export function detectPlatform(icon: string | null, name: string): SocialPlatform | null {
  const key = (icon ?? name).toLowerCase()
  if (key.includes("facebook"))  return "facebook"
  if (key.includes("instagram")) return "instagram"
  if (key.includes("tiktok"))    return "tiktok"
  if (key.includes("linkedin"))  return "linkedin"
  if (key.includes("youtube"))   return "youtube"
  if (key.includes("whatsapp"))  return "whatsapp"
  if (key.includes("twitter") || key.includes("x.com")) return "twitter"
  if (key.includes("snapchat"))  return "snapchat"
  return null
}
